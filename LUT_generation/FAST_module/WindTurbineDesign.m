    BladeColors = permute(BladeColors,[1,3,2]);
end

if NBlades == 1
    do = pi/2;
else
    do = pi - pi/NBlades;
end

for B = 1:NBlades
    Rx = [1, 0,      0; ...
          0, cos(B*o + do),-sin(B*o + do); ...
          0, sin(B*o + do), cos(B*o + do)];
    A = [reshape(x,1,[]); reshape(y,1,[]); reshape(z,1,[])];
    A = Rc * A;
    A(1,:) = A(1,:) - Hub_Overhang;
    A = Ry * Rx * A;
