    [x,y,z] = sphere(N(2)-1);
    x = x * Blade_Chord(1)/4;
    y = y * Blade_Chord(1)/2;
    z = z * Blade_Chord(1)/4 - Blade_Radius(end)/4;
    N = size(x);
    A = [reshape(x,1,[]); reshape(y,1,[]); reshape(z,1,[])];
    A = Rc * A;
    A(1,:) = A(1,:) - Hub_Overhang;
    A = Ry * Rx * A;
    x = reshape(A(1,:),N);
    y = reshape(A(2,:),N);
    z = reshape(A(3,:),N) + Hub_Height;
    surf(x,y,z, ...
        'FaceColor', handles.BladePaint, ...
        'EdgeColor', handles.EdgeColor, ...
