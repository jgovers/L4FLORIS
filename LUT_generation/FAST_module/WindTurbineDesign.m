%% Animate button
function Animate_Callback(hObject, eventdata, handles)

% Animation framerate
fps = 30;
closing = false;

% Get geometry from handles
Hub_Height = handles.Hub_Height;
Shaft_Tilt = handles.Shaft_Tilt;
NBlades = handles.NBlades;

% Rotating parts
if NBlades == 1
    NParts = 3 + 1;
else
    NParts = NBlades + 1;
end

% Rotation matrices
t = Shaft_Tilt*pi/180;
o = handles.RotationSpeed/fps * 2*pi/60;
R = ...
    [cos(t),  0,       sin(t); ...
     0,       1,       0; ...
    -sin(t),  0,       cos(t)] * ...
    [1,       0,       0; ...
     0,       cos(o), -sin(o); ...
     0,       sin(o),  cos(o)] * ...
    [cos(-t), 0,       sin(-t); ...
     0,       1,       0; ...
    -sin(-t), 0,       cos(-t)];

% Change button text
if get(hObject,'Value')
    set(hObject, 'String', 'Stop')
else
    set(hObject, 'String', 'Animate')
end

% Draw frames
frametime = tic;
while get(hObject,'Value')

    camera = get(handles.TurbinePlot, 'CameraPosition');
    
    while toc(frametime) < 1/fps 
    end
    frametime = tic;

    drawtime = tic;
    Parts = get(handles.TurbinePlot, 'Children');
    for i = 1:NParts
        Part = Parts(i);
        N = size(get(Part,'XData'));
        A = [...
            reshape(get(Part,'XData'),1,[]); ...
            reshape(get(Part,'YData'),1,[]); ...
            reshape(get(Part,'ZData'),1,[]) - Hub_Height];
        A = R*A;
        set(Part,...
            'XData', reshape(A(1,:),N), ...
            'YData', reshape(A(2,:),N), ...
            'ZData', reshape(A(3,:),N) + Hub_Height);
    end

    drawnow
    
    if strcmp(get(hObject, 'String'), 'Closing...')
        closing = true;
        break
    end
    
    if toc(drawtime) > 1/fps
        if isequal(camera, get(handles.TurbinePlot, 'CameraPosition'))
            if fps > 1
                fps = fps - 1;
                o = handles.RotationSpeed/fps * 2*pi/60;
                R = ...
                    [cos(t),  0,       sin(t); ...
                     0,       1,       0; ...
                    -sin(t),  0,       cos(t)] * ...
                    [1,       0,       0; ...
                     0,       cos(o), -sin(o); ...
                     0,       sin(o),  cos(o)] * ...
                    [cos(-t), 0,       sin(-t); ...
                     0,       1,       0; ...
                    -sin(-t), 0,       cos(-t)];
            end
        end
    elseif toc(drawtime) < 1/30
        if isequal(camera, get(handles.TurbinePlot, 'CameraPosition'))
            if fps < 30
                fps = fps + 1;
                o = handles.RotationSpeed/fps * 2*pi/60;
                R = ...
                    [cos(t),  0,       sin(t); ...
                     0,       1,       0; ...
                    -sin(t),  0,       cos(t)] * ...
                    [1,       0,       0; ...
                     0,       cos(o), -sin(o); ...
                     0,       sin(o),  cos(o)] * ...
                    [cos(-t), 0,       sin(-t); ...
                     0,       1,       0; ...
                    -sin(-t), 0,       cos(-t)];
            end
        end
    end

end

if closing
    close all force
end

